package com.company;

public class Point {
    /*
    Represents any point (2d Vector) in the game.
     */
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
